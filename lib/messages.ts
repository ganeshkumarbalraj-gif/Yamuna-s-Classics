export function getProductInquiryMessage(
  productName: string
) {
  return `Hello Yamuna,

I came across "${productName}" on your website and I'm interested in it.

Could you please share:

• Price
• Available colours
• Customization options
• Delivery timeline
• Ordering process

Looking forward to hearing from you.

Thank you!`;
}

